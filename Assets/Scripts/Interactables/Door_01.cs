using System.Collections;
using System.Collections.Generic;
using UnityEngine;

/// <summary>
/// 门交互类
/// 实现Interactable接口，处理玩家与门的交互
/// </summary>
/// <remarks>
/// C#特性说明:
/// - Interactable: 自定义接口，定义交互行为
/// - [SerializeField]: Unity序列化特性，让私有字段在Inspector中可编辑
/// - Animator: Unity动画组件，控制动画状态
/// </remarks>
public class Door : Interactable
{
    // ============================================================================
    // Unity特性说明: [SerializeField]
    // ============================================================================
    // [SerializeField]是Unity的序列化特性
    // 让私有字段可以在Inspector面板中编辑
    // 类似C++中需要手动实现编辑器UI，C#通过特性自动处理
    // ============================================================================
    
    [SerializeField] private GameObject door;
    private bool doorOpen;
    
    // ============================================================================
    // Unity生命周期方法: Start()
    // ============================================================================
    // Start在Awake之后、第一帧更新之前调用
    // 适合用于初始化组件和设置初始状态
    // ============================================================================
    
    // Start is called before the first frame update
    void Start()
    {
        
    }
    
    // ============================================================================
    // Unity生命周期方法: Update()
    // ============================================================================
    // Update每帧调用一次，用于处理持续的游戏逻辑
    // 适合用于检测输入、更新状态等
    // ============================================================================
    
    // Update is called once per frame
    void Update()
    {
        
    }
    
    // ============================================================================
    // Unity特性说明: override关键字
    // ============================================================================
    // override用于重写基类的方法
    // Interactable是基类的方法，这里重写实现门的交互逻辑
    // ============================================================================
    
    /// <summary>
    /// 玩家与门交互的方法
    /// 切换门的开关状态并播放相应动画
    /// </summary>
    /// <remarks>
    /// Unity特性: GetComponent、Animator
    /// - GetComponent: 获取对象上的组件
    /// - Animator.SetBool: 设置动画器的布尔参数
    /// </remarks>
    protected override void Interact()
    {
        doorOpen = !doorOpen;
        Debug.Log("Interacting with Door");
        
        // GetComponent获取对象上的Animator组件
        // SetBool设置动画器的布尔参数，控制动画状态
        door.GetComponent<Animator>().SetBool("IsOpen", doorOpen);
        
    }
}
